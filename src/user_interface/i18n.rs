pub fn get_i18n(lang: UiLanguage) -> &'static I18n {
    match lang {
        UiLanguage::Thai => &TH,
        UiLanguage::English => &EN,
        UiLanguage::System => {
            let locale = sys_locale::get_locale().unwrap_or_else(|| "en".to_string());
            if locale.starts_with("th") {
                &TH
            } else {
                &EN
            }
        }
    }
}
